//Math

const pi = Math.PI
console.log('Valor de pi: ', pi)

//primeiro numero e base e o segundo expoente
const potencia = Math.pow(2,10)
console.log('2 elevado a 10: ', potencia)

const logaritimo = Math.log(10)
console.log('log de 10: ', logaritimo)

const minimo = Math.min(10,20,5)
console.log('Qual o menor numero (10,20,5): ', minimo) 

const maximo = Math.max(60,30,45,61)
console.log('Qual o maior numero (60,30,45, 61): ', maximo)

//se nao multiplicar por nada o Math.random retorna um numero de 0 a 1
const aleatorio = Math.random() * 100
console.log ('Numero aleatorio de 0 a 100: ', aleatorio)

const raizQuadrada = Math.sqrt(80)
console.log('Raiz quadrade de 80: ',raizQuadrada)

const seno = Math.sin(60)
console.log('Seno de 60: ',seno)

const cosseno = Math.cos(60)
console.log('Cosseno de 60: ', cosseno)

const tangente = Math.tan(90)
console.log('Tangente de 90: ',tangente)
